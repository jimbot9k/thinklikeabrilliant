		<h1>You Think Like a Brilliant.</h1>
		<p class="subtitle">A comprehensive, peer-reviewed, and frankly exhausting deep-dive into what that means for you, society, and the pelican.</p>

		<h2>Part I: What Is a Brilliant, Exactly?</h2>
		<p>A Brilliant is not simply a person who is smart. Many smart people are idiots. A Brilliant is a person who has made peace with not knowing things, which is ironic, because not knowing things is extremely uncomfortable and most people would rather die than sit with an unanswered question for more than forty-five seconds.</p>
		<p>The Brilliant sits with it. The Brilliant makes a cup of tea, pulls up a chair, and says to the unanswered question: "Hello. You look complicated. I would like to understand you. This may take some time. I have cleared my schedule." The unanswered question is, frankly, taken aback. Nobody has ever treated it this way before. Most people just Google the first result and move on. The Brilliant reads the citations.</p>
		<p>This is both a gift and a curse, because the Brilliant is often late to things.</p>

		<h2>Part II: The Origin of the Brilliant</h2>
		<p>Historians disagree on when the first Brilliant appeared. Some say it was Aristotle, who famously said that the more you know, the more you know you don't know, which is the kind of thing a Brilliant says right before everyone at the party moves to a different room.</p>
		<p>Others say it was whoever invented the double-blind study, because that person looked at the entire history of human self-deception and said "what if we made it harder to lie to ourselves" and everyone else said "why would we want that" and the Brilliant said "I'll explain" and proceeded to do so for forty years.</p>
		<p>The Brilliant has always existed. They were the one in the cave asking "but WHY does fire burn" while everyone else was just trying to cook a mammoth. They were annoying then too, but they were also correct, and eventually people came around.</p>

		<h2>Part III: Key Traits of the Brilliant (Documented)</h2>
		<p><strong>Trait 1: Comfort with Being Wrong.</strong> The Brilliant has discovered that being wrong is not, in fact, fatal. This was a surprise to them also. They were wrong about something in 2019 and they did not die. They updated their belief system. They moved on. They now actively seek out situations where they might be wrong, which their friends find exhausting and which is technically just called "having intellectual standards."</p>
		<p><strong>Trait 2: The Inability to Forward Unverified Statistics.</strong> When the group chat goes feral over a statistic from an account called @TruthZone47, the Brilliant feels a physical sensation in their chest. They open the thread. They look for sources. They find that the statistic was made up in 2014 by a man in Portsmouth who was having a bad day. They post the correction. Nobody reacts to it. They post it anyway. They will always post it anyway.</p>
		<p><strong>Trait 3: Suspiciously Good at Estimating Things.</strong> The Brilliant has, at some point, read something about Fermi estimation and has never fully recovered. They can now tell you approximately how many piano tuners are in Chicago, how long it would take to walk to the moon, and how many litres of custard you would need to fill an Olympic swimming pool. Nobody has asked for any of this. The Brilliant offers it freely.</p>
		<p><strong>Trait 4: The Note-Taking.</strong> The Brilliant takes notes. Not just at work or in lectures. Everywhere. On their phone. In the margins of receipts. On their arm if necessary. They have a notes folder with 847 unsorted voice memos. They have a document called "ideas - misc" that is 34,000 words long. They have never looked at any of it. The act of capturing the thought is the point. The thought is safe. The thought has been recorded. The Brilliant can now sleep.</p>
		<p><strong>Trait 5: A Complicated Relationship With Confidence.</strong> The Brilliant is confident, but not in the loud way. They are confident in the specific, load-bearing way. They will say "I'm not sure about that" more than any other type of person, and paradoxically this makes them more reliable than people who say "definitely" about everything. If a Brilliant says they're sure, they're sure. You can build a house on it. You can take it to court. You can bring it up at Thanksgiving.</p>

		<h2>Part IV: The Brilliant in Social Situations</h2>
		<p>The Brilliant at a dinner party is a fascinating specimen. They arrive slightly late because they were reading one more thing. They bring wine they researched. They listen more than they speak, which people find either refreshing or unsettling depending on whether they are also a Brilliant.</p>
		<p>When a topic comes up that they know little about, the Brilliant says "I don't know much about that, tell me more." When a topic comes up that they know a lot about, they say "there's actually some interesting nuance here" and then immediately sense the room's energy and decide to save it for someone who wants to know.</p>
		<p>The Brilliant has seventeen opinions they have been waiting for the right moment to share. That moment has not come. They have had these opinions since 2021. They are still valid. They are keeping them.</p>

		<h2>Part V: The Brilliant and the Pelican</h2>
		<p>The pelican question — the foundational question of this assessment — is crucial. Because the Brilliant, upon finding a pelican on their chest holding their phone, did not panic. They observed. They assessed. They asked: what does this pelican want? Is it the phone, specifically, or does the phone represent something? Is this pelican a metaphor? Is this a Thursday? These are reasonable questions.</p>
		<p>The Brilliant knows that most problems, including pelican-based ones, have an internal logic. Find the logic, find the solution. The pelican has motivations. The pelican has a context. The pelican is not simply a chaos agent — it is a system, and systems can be understood.</p>
		<p>The Brilliant got their phone back. They also learned three facts about pelican behaviour that they did not know before. They wrote them down in their notes app. The document is called "pelican - misc." It is 400 words long. There is a follow-up document called "pelican - further reading." They do not regret this.</p>

		<h2>Part VI: What This Means For Your Future</h2>
		<p>You are going to be okay. More than okay, actually. You are going to be the person in the room who knows what they're talking about, who changes their mind when the evidence changes, and who asks questions that make other people feel slightly seen and slightly uncomfortable at the same time.</p>
		<p>You will be right about things before other people. This is both a superpower and a source of unending low-level frustration that you have learned to manage through journaling, walks, and occasionally being right out loud when the stakes are high enough to justify it.</p>
		<p>The owls respect you. The horse's email is waiting. The wizard's offer stands.</p>
		<p>Go be brilliant. You already are.</p>
